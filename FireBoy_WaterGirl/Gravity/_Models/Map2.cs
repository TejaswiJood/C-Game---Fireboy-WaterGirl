        public Map2()
        {
            _target1 = new(Globals.GraphicsDevice, tiles1.GetLength(1) * TILE_SIZE, tiles1.GetLength(0) * TILE_SIZE);

            var tile1tex = Globals.Content.Load<Texture2D>("Tiles/tile_a");
            var tile2tex = Globals.Content.Load<Texture2D>("Tiles/tileb1");
            var tile3tex = Globals.Content.Load<Texture2D>("Tiles/tile3");
            var tile4tex = Globals.Content.Load<Texture2D>("Tiles/tile_4");
            var tile5tex = Globals.Content.Load<Texture2D>("Tiles/tile_5");
            var tile6tex = Globals.Content.Load<Texture2D>("Tiles/tile_6");
            var tile7tex = Globals.Content.Load<Texture2D>("Tiles/tile_5a");
            var water = Globals.Content.Load<Texture2D>("water");
            var lava = Globals.Content.Load<Texture2D>("Lava");
            var grey = Globals.Content.Load<Texture2D>("Tiles/gold");
            var black = Globals.Content.Load<Texture2D>("Tiles/end");
            var finisher = Globals.Content.Load<Texture2D>("Colours/black");

            Globals.GraphicsDevice.SetRenderTarget(_target1);
            Globals.GraphicsDevice.Clear(Color.Transparent);
            Globals.SpriteBatch.Begin();

            for (int x = 0; x < tiles1.GetLength(0); x++)
            {
                for (int y = 0; y < tiles1.GetLength(1); y++)
                {
                    if (tiles1[x, y] == 0) continue;
                    var posX = y * TILE_SIZE;
                    var posY = x * TILE_SIZE;
                    Texture2D tex;

                    if (tiles1[x, y] == 1)
                    {
                        tex = tile1tex;
                    }
                    else if (tiles1[x, y] == 2)
                    {
                        tex = tile2tex;
                    }
                    else if (tiles1[x, y] == 3)
                    {
                        tex = tile3tex;
                    }
                    else if (tiles1[x, y] == 4)
                    {
                        tex = tile4tex;
                    }
                    else if (tiles1[x, y] == 5)
                    {
                        tex = tile5tex;
                    }
                    else if (tiles1[x, y] == 6)
                    {
                        tex = tile6tex;
                    }
                    else if (tiles1[x, y] == 7)
                    {
                        tex = tile7tex;
                    }
                    else if (tiles1[x, y] == 8)
                    {
                        tex = black;
                    }
                    else if (tiles1[x, y] == 11)
                    {
                        tex = lava;
                    }
                    else if (tiles1[x, y] == 12)
                    {
                        tex = water;
                    }
                    else if (tiles1[x,y] == 13)
                    {
                        tex = finisher;
                    }
                    else
                    {
                        tex = grey;
                    }
                    Colliders1[x, y] = new(posX, posY, TILE_SIZE, TILE_SIZE);
                    Acid1[x,y] = new(posX, posY, TILE_SIZE,TILE_SIZE);
                    Finish1[x,y] = new(posX,posY, TILE_SIZE, TILE_SIZE);
                    Water[x, y] = new(posX, posY, TILE_SIZE, TILE_SIZE);
                    Lava[x, y] = new(posX, posY, TILE_SIZE, TILE_SIZE);
                    Globals.SpriteBatch.Draw(tex, new Vector2(posX, posY), Color.White);
                }
            }

            Globals.SpriteBatch.End();
            Globals.GraphicsDevice.SetRenderTarget(null);
        }

        /// <summary>
        /// This will add the Collider property to the appropriate tiles
        /// </summary>
        /// <param name="bounds"></param>
        /// <returns></returns>
        public static List<Rectangle> GetNearestColliders(Rectangle bounds)
        {
            int leftTile = (int)Math.Floor((float)bounds.Left / TILE_SIZE);
            int rightTile = (int)Math.Ceiling((float)bounds.Right / TILE_SIZE) - 1;
            int topTile = (int)Math.Floor((float)bounds.Top / TILE_SIZE);
            int bottomTile = (int)Math.Ceiling((float)bounds.Bottom / TILE_SIZE) - 1;

            leftTile = MathHelper.Clamp(leftTile, 0, tiles1.GetLength(1) - 1);
            rightTile = MathHelper.Clamp(rightTile, 0, tiles1.GetLength(1) - 1);
            topTile = MathHelper.Clamp(topTile, 0, tiles1.GetLength(0) - 1);
            bottomTile = MathHelper.Clamp(bottomTile, 0, tiles1.GetLength(0) - 1);


            List<Rectangle> result = new();

            for (int x = topTile; x <= bottomTile; x++)
            {
                for (int y = leftTile; y <= rightTile; y++)
                {
                    if (tiles1[x, y] == 1 || tiles1[x, y] == 2) { result.Add(Colliders1[x, y]); }
                }
            }

            return result;
        }

